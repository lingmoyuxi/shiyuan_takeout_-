package com.shiyuan.model;

public class Thirdlogin {
    private Long thirdloginId;

    private Long userId;

    private String thirdloginKey;

    public Long getThirdloginId() {
        return thirdloginId;
    }

    public void setThirdloginId(Long thirdloginId) {
        this.thirdloginId = thirdloginId;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public String getThirdloginKey() {
        return thirdloginKey;
    }

    public void setThirdloginKey(String thirdloginKey) {
        this.thirdloginKey = thirdloginKey;
    }
}